#include <iostream>

int main()
{
  // Default Values
  int cplusplusInt;
  double cplusplusDouble;
  long cplusplusLong;
  float cplusplusFloat;
  bool cplusplusBool;

  std::cout << "C++ Default Values:" << std::endl;
  std::cout << "Uninitialized int: " << cplusplusInt << std::endl;
  std::cout << "Uninitialized double: " << cplusplusDouble << std::endl;
  std::cout << "Uninitialized long: " << cplusplusLong << std::endl;
  std::cout << "Uninitialized float: " << cplusplusFloat << std::endl;
  std::cout << "Uninitialized bool: " << cplusplusBool << std::endl;
  return 0;
}